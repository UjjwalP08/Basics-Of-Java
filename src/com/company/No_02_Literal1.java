package com.company;

/**
* Literals:-
 *          A constant value which can be assigned to the variable is called Literal
 *
 *          Type of Literal in Java
 *          1) Integer Literal
 *          2) Float Literal
 *          3) Double Literal
 *          4) Character Literal
 *          5) Boolean Literal
 *          6) String Literal
* */
public class No_02_Literal1 {

    public static void main(String[] args) {

        // Integer Literal
        int i = 90;

        // Integer Literal
        short s =90;

        // Integer Literal
        byte b =34;

        // Integer Literal
        long l =9999999999999L;

        // Float Literal
        float f = 0.3f;

        // Double Literal
        double d =45.34;

        // Character Literal
        char c = 'U';

        // Boolean Literal
        boolean bo = true;

        // String Literal
        String str="This is a string";

        System.out.println(str);

    }
}

