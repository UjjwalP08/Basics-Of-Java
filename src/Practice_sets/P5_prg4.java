// Write the java program to print the multiplication  table of 10 in revers order
package Practice_sets;

public class P5_prg4 {
    public static void main(String[] args) {
        for (int i =10;1<=i;i--)
        {
//            System.out.printf("10 Table is 10*%d = %d\n", i,10*i);
            System.out.println("10 Table is 10*"+i+" = "+10*i);
        }
    }
}
